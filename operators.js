// Arithmatics Operators 

let a = 10;
let b = 5;

console.log("a + b =",a+b);
console.log("a - b =",a-b);
console.log("a * b =",a*b);
console.log("a / b =",a/b);
// Modules %
console.log("a % b =",a%b);
// Exponentiation a pow b (a ** b)
console.log("a ** b =",a ** b);
// Increment 
console.log("++a =",++a);
// Decrement
console.log("--a =",--a);





